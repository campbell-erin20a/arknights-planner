
import * as s from './seconds';


const MILLIS = 1000;

export const SECONDS = s.SECONDS * MILLIS;
export const SECOND = SECONDS;

export const MINUTES = s.MINUTES * MILLIS;
export const MINUTE = MINUTES;

export const HOURS = s.HOURS * MILLIS;
export const HOUR = HOURS;

export const DAYS = s.DAYS * MILLIS;
export const DAY = DAYS;
